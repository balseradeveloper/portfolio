let currentLanguage = localStorage.getItem('language') || 'es';

function translatePage() {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLanguage][key]) {
            element.textContent = translations[currentLanguage][key];
        }
    });

    // Actualizar el bot√≥n de idioma
    const languageBtn = document.getElementById('toggle-language');
    if (currentLanguage === 'es') {
        languageBtn.textContent = translations[currentLanguage]['english'];
    } else {
        languageBtn.textContent = translations[currentLanguage]['spanish'];
    }

    // Actualizar el bot√≥n de modo oscuro tambi√©n
    const darkModeBtn = document.getElementById('toggle-dark-mode');
    const isDark = document.body.classList.contains('dark-mode');
    if (isDark) {
        darkModeBtn.textContent = translations[currentLanguage]['modo-claro'];
    } else {
        darkModeBtn.textContent = translations[currentLanguage]['modo-oscuro'];
    }
}

function toggleLanguage() {
    currentLanguage = currentLanguage === 'es' ? 'en' : 'es';
    localStorage.setItem('language', currentLanguage);
    translatePage();
}

// Hacer las traducciones disponibles globalmente para darkmode.js
window.translations = translations;

// Inicializar
document.addEventListener('DOMContentLoaded', () => {
    translatePage();

    const languageBtn = document.getElementById('toggle-language');
    languageBtn.addEventListener('click', toggleLanguage);
});